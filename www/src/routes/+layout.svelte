<script lang="ts">
    import { writable } from "svelte/store";
    import "../app.css";
    import { onMount } from "svelte";
    let year = new Date().getFullYear();

    const items = [
        { url: "/", text: "BÃºsqueda RUCs" },
        { url: "/download", text: "Bajar archivos" },
        { url: "/snippets", text: "Code Snippets" },
        { url: "/about", text: "Acerca de" },
    ];
    
    const menuVisible = writable(false);
    const current = writable(items[0]);

    function clickGoTo(to: typeof items[0]) {
        current.set(to);
        menuVisible.set(false);
    }
    onMount(() => {
        const pathname = window.location.pathname;
        current.set(items.find((item) => item.url === pathname) || items[0]);
    });
</script>

<div
    class="flex flex-col items-center justify-flex-start min-h-screen bg-gray-900"
>
    <div
        class="sticky top-0 z-40 flex-none w-full mx-auto border-b border-gray-200 dark:border-gray-600 bg-gray-800"
    >
        <nav class=" border-gray-200 bg-gray-800">
            <div
                class="md:hidden flex justify-between pl-2 pr-2"
            >
                <h1 class="text-xl text-white font-bold m-2">
                    {$current.text}
                </h1>
                <button
                    on:click={() => menuVisible.update((v) => !v)}
                    data-collapse-toggle="navbar-default"
                    type="button"
                    class="inline-flex items-center p-2 w-10 h-10 justify-center text-sm rounded-lg focus:outline-none focus:ring-2 focus:ring-gray-200 text-gray-400 hover:bg-gray-700 focus:ring-gray-600"
                    aria-controls="navbar-default"
                    aria-expanded="false"
                >
                    <span class="sr-only">Open main menu</span>
                    <svg
                        class="w-5 h-5"
                        aria-hidden="true"
                        xmlns="http://www.w3.org/2000/svg"
                        fill="none"
                        viewBox="0 0 17 14"
                    >
                        <path
                            stroke="currentColor"
                            stroke-linecap="round"
                            stroke-linejoin="round"
                            stroke-width="2"
                            d="M1 1h15M1 7h15M1 13h15"
                        />
                    </svg>
                </button>
            </div>
            <div
                class="max-w-screen-xl flex flex-wrap items-center justify-around mx-auto"
            >
                <div
                    class:hidden={!$menuVisible}
                    class="w-full md:block md:w-auto p-4"
                    id="navbar-default"
                >
                    <ul
                        class="font-medium flex flex-col p-4 md:p-0 mt-4 border border-gray-100 rounded-lg bg-gray-50 md:flex-row md:space-x-8 rtl:space-x-reverse md:mt-0 md:border-0 md:bg-white dark:bg-gray-800 md:dark:bg-gray-900 dark:border-gray-700"
                    >
                        {#each items as item (item.url)}
                            <li>
                                <a
                                    href={item.url}
                                    on:click={() => clickGoTo(item)}
                                    class={item.url === $current.url
                                        ? "block py-2 px-3 text-white bg-blue-700 rounded md:bg-transparent md:text-blue-700 md:p-0 dark:text-white md:dark:text-blue-500"
                                        : "block py-2 px-3 text-gray-900 rounded hover:bg-gray-100 md:hover:bg-transparent md:border-0 md:hover:text-blue-700 md:p-0 dark:text-white md:dark:hover:text-blue-500 dark:hover:bg-gray-700 dark:hover:text-white md:dark:hover:bg-transparent"}
                                    aria-current="page">{item.text}</a
                                >
                            </li>
                        {/each}
                    </ul>
                </div>
            </div>
        </nav>
    </div>

    <div class="p-4 text-center flex-grow" style="max-width: 100vw;">
        <slot />
    </div>

    <footer class="w-full text-center p-4 bg-gray-900 text-gray-200">
        &copy; {year} Arturo Volpe
    </footer>
</div>
